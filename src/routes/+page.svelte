						<h5 class="wp-block-heading has-black-color has-text-color">
							<strong>The Story of Ecopoint</strong>
						</h5>

						<p>
							In 2020, after climbing the tradroute ‘Prinzip Hoffnung’ by public transport, Lena
							Müller had the idea to give this sustainable approach to climbing a name. The reason
							being that language shapes our thoughts and cultures – including our climbing culture.
							Hence creating <em>new </em>terms could support our actions, such as our approach to
							climbing. She suggested the term <em>greenpoint</em>, not being aware that the term
							already existed. Together with Sofie Paulus they now want to introduce this concept
							under the name <em>ecopoint</em> to raise awareness and promote sustainable climbing.
						</p>
